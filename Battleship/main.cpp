#include "Board.h"
#include "PointCollection.h"
#include "Ship.h"
#include <iostream>

using namespace std;

int main() {

    
    srand(time(NULL));
    Board battleBoard;
    int x;
    int y;
    
    cout << "Welcome to Battleship.\n\n";
    
    while(true) {
        
        battleBoard.display();
        
        std::cout << "Enter a coordinate: ";
        std::cin >> x >> y;
        
        if(battleBoard.fireShot(x, y))
            cout << "Hit!";
        else {
            cout << "Miss.";
        }
        
        cout << endl;
        
        if(battleBoard.unsunkShipCount() == 0)
        {
            battleBoard.display();
            break;
        }
        
    }
    
    cout << "Thank you for playing the game!\n";
    return 0;
}

